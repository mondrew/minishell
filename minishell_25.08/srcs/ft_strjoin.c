#include "minishell.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*res;
	size_t	i;
	size_t	j;
	size_t	res_len;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	res_len = ft_strlen(s1) + ft_strlen(s2);
	res = malloc(res_len * sizeof(char) + sizeof(char));
	if (res == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		res[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		res[i + j] = s2[j];
		j++;
	}
	res[i + j] = '\0';
	return (res);
}
